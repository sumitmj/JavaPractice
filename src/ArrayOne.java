public class ArrayOne {
    public static void main(String[] args) {

        // Declaration and Instantiation of arrays
        int[] num = new int[4];
        // Initialization
        num[0] = 1;
        num[1] = 2;
        num[2] = 3;
        num[3] = 4;
        // Traversing array
        for (int j : num) {
            System.out.println(j);
        }
    }
}
